export async function getStaticProps(context) {
  try {
    const res = await fetch(`http://${process.env.BACKEND_API}/package`);
    const message = await res.json();
    return {
      props: { message }
    };
  } catch (error) {
    console.error(error);
    return {
      props: {
        message: "An error occurred while fetching data from the backend."
      }
    };
  }
}

